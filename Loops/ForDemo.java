
public class ForDemo {
    public static void main(String[] args) { 
        //for loop executes a limited number of times

        //We want to count from 0 to ten
        //We declare a counter, we iterate the loop while tihs condition is true, then we can increment or decrement our index


        for(int i = 10; i >= 0;) { 
            System.out.println(i);
            i--; 

        }
        System.out.println("Happy new year!"); 
    }
}
